<template>

    <transition name="fade">
        <div class="vue-modal" v-show="open">
            <transition name="drop-in">
                <div class="vue-modal-inner" v-show="open">
                    <div class="vue-modal-content">

                        <div class="row">
                            <div class="col-md-12 mt-2">
                                <div class="card">
                                    <div class="card-body">

                                        <div class="px-3">
                                            <div class="form-body">

                                                <form>
                                                    <div class="row">
                                                        <div class="col-md-6">
                                                            <div class="form-group">
                                                                <label>Title</label>
                                                                <input v-model="form.title" type="text" id="name"
                                                                       class="form-control pl-2" required>
                                                            </div>
                                                        </div>

                                                        <div class="col-md-6">
                                                            <div class="form-group pl-1">

                                                                <label>image</label><br>
                                                                <input type="file" @change="uploadImage"
                                                                       class="input-bg" required>

                                                                <!--If new module on image upload-->
                                                                <div v-if="module === undefined && imageErrorMessage === '' && form.image !== null">
                                                                    <img :src="imagePreview" width="100"/>
                                                                    <span @click="form.image = null"
                                                                          class="pl-1 fa fa-times text-danger"
                                                                          title="Remove image"></span>
                                                                </div>

                                                                <!--If updating module and image not yet uploaded-->
                                                                <div v-if="module !== undefined && imageErrorMessage === '' && form.image === null">
                                                                    <img :src="module.image" width="100"/>
                                                                </div>

                                                                <!--If updating module and image uploaded-->
                                                                <div v-else-if="module !== undefined && imageErrorMessage === '' && form.image !== null">
                                                                    <img :src="imagePreview" width="100"/>
                                                                    <span @click="form.image = null"
                                                                          class="pl-1 fa fa-times text-danger"
                                                                          title="Remove image"></span>
                                                                </div>

                                                                <p v-if="imageErrorMessage !== ''"
                                                                   class="text-center text-danger">
                                                                    {{ imageErrorMessage }}
                                                                </p>

                                                            </div>
                                                        </div>
                                                    </div>

                                                    <div class="row">
                                                        <div class="col-12">
                                                            <div class="form-group">
                                                                <label>Learning Outcome</label>
                                                                <ckeditor :editor="editor" v-model="form.description"
                                                                          :config="editorConfig" class="input-bg"></ckeditor>
                                                            </div>
                                                        </div>

                                                        <div class="col-12">
                                                            <div class="form-group">
                                                                <label>Trainers</label>
                                                                <ckeditor :editor="editor" v-model="form.trainers"
                                                                          :config="editorConfig" class="input-bg"></ckeditor>
                                                            </div>
                                                        </div>

                                                        <div class="col-12">
                                                            <div class="form-group">
                                                                <label>Teaching Methods</label>
                                                                <ckeditor :editor="editor" v-model="form.training_methods"
                                                                          :config="editorConfig" class="input-bg"></ckeditor>
                                                            </div>
                                                        </div>

                                                        <div class="col-md-6 pr-1">
                                                            <div class="form-group">
                                                                <label>Status</label>
                                                                <select v-model="form.status" class="form-control" required>
                                                                    <option value="">Select</option>
                                                                    <option value="1">Publish</option>
                                                                    <option value="0">Draft</option>
                                                                </select>
                                                            </div>
                                                        </div>

                                                        <div class="col-md-6 pl-1">
                                                            <div class="form-group">
                                                                <label>Order (0, being the first)</label>
                                                                <input v-model="form.sort" type="number" id="name"
                                                                       class="form-control" :placeholder="form.sort">
                                                            </div>
                                                        </div>

                                                    </div>

                                                    <div class="col-md-12 center-inside">
                                                        <button v-if="module === undefined"
                                                                @click.prevent="submitModule"
                                                                class="btn btn-primary btn-md p-1 mr-1">Submit</button>
                                                        <button v-else @click.prevent="updateModule(module.id)"
                                                                class="btn btn-primary btn-md mr-1 p-1">Update</button>
                                                        <button @click.prevent="emitCloseModal"
                                                                class="btn btn-danger btn-md p-1">Close</button>
                                                    </div>

                                                </form>

                                            </div>
                                        </div>

                                        <p class="text-center text-danger"
                                              v-for="(error, index) in errors" :key="index">
                                            {{ error}}
                                        </p>

                                    </div>
                                </div>
                            </div>
                        </div>

                    </div>
                </div>
            </transition>
        </div>
    </transition>

</template>

<script>
// import CkEditor in current component to work
import CKEditor from '@ckeditor/ckeditor5-vue';
import ClassicEditor from '@ckeditor/ckeditor5-build-classic';

import {onMounted, onUnmounted, reactive, ref} from 'vue';
import SweetAlertService from "../../../services/sweet-alert-service";
import axios from "axios";

export default {
    props: {
        module: {
            type: Object,
            required: false,
        },
        open: Boolean
    },

    components: {
        // Use the <ckeditor> component in this view.
        ckeditor: CKEditor.component,
    },

    // Context for emit
    setup(props, context){

        const module = ref(props.module);
        const form = reactive({
            title: props.module !== undefined ? module.value.title : '',
            image: null,
            description: props.module !== undefined ? module.value.description : '',
            trainers: props.module !== undefined ? module.value.trainers : '',
            training_methods: props.module !== undefined ? module.value.training_methods : '',
            sort: props.module !== undefined ? module.value.sort : "0",
            status: props.module !== undefined ? module.value.status : ''
        });
        const editor = ClassicEditor;
        const editorConfig = ref({});
        const errors = ref([]);
        const imageErrorMessage = ref('');
        const imagePreview = ref(null);

        const emitCloseModal = () => {
            console.log("emit close modal");
            context.emit("emitting-close-modal", 'Close modal');
        }

        const handleKeyup = (event) => {
            if(event.keyCode === 27){
                emitCloseModal();
            }
        }

        const uploadImage = (event) => {
            validateImage(event);
            //Assign image and path to this variable
            form.image = event.target.files[0];
            // create base64 version display of image
            imagePreview.value = URL.createObjectURL(event.target.files[0]);
        }

        const validateImage = (event) => {
            // Validate Image
            let allowedExtensions = /(\.jpg|\.jpeg|\.png)$/i;
            if(!allowedExtensions.exec(event.target.files[0].name)){
                imageErrorMessage.value = 'Incorrect image format';
                console.log(imageErrorMessage.value);
                form.image = null;
                return false;
            }else{
                imageErrorMessage.value = '';
            }

            if(event.target.files[0].size > 5000000){
                imageErrorMessage.value = 'File too large, 5mb max.';
                form.image = null;
                return false;
            }else{
                imageErrorMessage.value = '';
            }
        }

        const submitModule = async () => {
            SweetAlertService.formLoading(Swal, 'Please wait', 'Submitting...');

            const formData = new FormData();
            // iterate and add form data
            Object.keys(form).forEach(function(key) {
                console.log(key); // key
                console.log(form[key]); // value
                formData.append(key, form[key]);
            });

            await axios.post('/api/admin/modules/store', formData, {
                headers: {
                    'content-type': 'multipart/form-data',
                    "Authorization" : "Bearer " + localStorage.getItem('afc-admin-tk'),
                    'Accept' : 'application/json',
                }
            }).then((response) => {
                if (response.data.success) {
                    errors.value = []; // Empty error messages
                    console.log("Emitting Submitted Module");
                    context.emit("emitting-submitted-module", response.data.module); // Emit created module to parent component (AdminModuleView)

                    SweetAlertService.formSuccess(Swal, 'Submitted', 2500);
                    SweetAlertService.formEmpty(form);
                } else {
                    if(response.data.message){
                        console.log(response.data.message);
                    }
                    if(response.data.errors.length){
                        errors.value = response.data.errors;
                        SweetAlertService.formError(Swal, 'one or more fields are required', 7000);
                    }
                }
            }).catch((error) => {
                console.log(error);
            });
        }

        const updateModule = async (id) => {
            SweetAlertService.formLoading(Swal, 'Please wait', 'Updating...');

            const formData = new FormData();
            // iterate and add form data
            Object.keys(form).forEach(function(key) {
                console.log(key); // key
                console.log(form[key]); // value
                formData.append(key, form[key]);
            });

            await axios.post('/api/admin/modules/update', formData, {
                headers: {
                    "Authorization" : "Bearer " + localStorage.getItem('afc-admin-tk'),
                    'Accept' : 'application/json',
                },
                params: {
                    id: id
                }
            }).then((response) => {
                if (response.data.success) {
                    console.log("Emitting Updated Module");
                    context.emit("emitting-updated-module", response.data.module);
                    SweetAlertService.formSuccess(Swal, 'Updated', 2500);

                } else {
                    SweetAlertService.formError(Swal, 'one or more fields are required', 7000);
                    if(response.data.message){
                        console.log(response.data.message);
                        console.log(response.data.error_message);
                    }
                    if(response.data.errors.length){
                        errors.value = response.data.errors;
                        console.log(response.data.errors);
                    }

                }
            }).catch((error) => {
                console.log(error);
            });
        }

        onMounted(() => {
            document.addEventListener("keyup", handleKeyup);
        });

        onUnmounted(() => {
            document.removeEventListener("keyup", handleKeyup);
        });

        return {
            form,
            module,
            editor,
            editorConfig,
            imageErrorMessage,
            imagePreview,
            errors,

            submitModule,
            updateModule,
            emitCloseModal,
            uploadImage,
            validateImage
        }
    }
}
</script>

