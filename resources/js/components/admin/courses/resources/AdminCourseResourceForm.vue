<template>

    <transition name="fade">
        <div class="vue-modal" v-show="open">
            <transition name="drop-in">
                <div class="vue-modal-inner" v-show="open">
                    <div class="vue-modal-content">

                        <div class="row">
                            <div class="col-md-12 mt-2">
                                <div class="card">
                                    <div class="card-body">

                                        <div class="px-3">
                                            <div class="form-body">

                                                <form>
                                                    <div class="row">
                                                        <div class="col-12">
                                                            <div class="form-group p-1">
                                                                <label>Title
                                                                    <span class="text-danger">*</span>
                                                                </label>
                                                                <input v-model="form.title" type="text" id="name"
                                                                       class="form-control pl-1" required>
                                                            </div>
                                                        </div>

                                                        <div class="col-md-6">
                                                            <div class="form-group pl-1">

                                                                <label>Document</label><br>
                                                                <input type="file" @change="uploadDocument"
                                                                       class="input-bg">

                                                                <!--If new resource on document upload-->
                                                                <div v-if="resource === undefined && documentErrorMessage === '' && form.document !== null">
                                                                    <p>{{ form.document.name }}</p>
                                                                    <span @click="form.document = null"
                                                                          class="pl-1 fa fa-times text-danger"
                                                                          title="Remove image"></span>
                                                                </div>

                                                                <!--If updating resource and document not yet uploaded-->
                                                                <div v-if="resource !== undefined && documentErrorMessage === ''">
                                                                    <a :href="resource.document" :download="resource.document">
                                                                        <i class="fa fa-download">Download</i>
                                                                    </a>
                                                                </div>

                                                                <!--If updating resource and document uploaded-->
                                                                <div v-else-if="resource !== undefined && documentErrorMessage === '' && form.document !== null">
                                                                    <p>{{ form.document.name }}</p>
                                                                    <span @click="form.document = null"
                                                                          class="pl-1 fa fa-times text-danger"
                                                                          title="Remove image"></span>
                                                                </div>

                                                                <p v-if="documentErrorMessage !== ''"
                                                                   class="text-center text-danger">
                                                                    {{ documentErrorMessage }}
                                                                </p>

                                                            </div>
                                                        </div>

                                                        <div class="col-md-6">
                                                            <div class="form-group p-1">
                                                                <label>Url</label>
                                                                <input v-model="form.url" type="text" id="name"
                                                                       class="form-control pl-1">
                                                            </div>
                                                        </div>

                                                    </div>

                                                    <div class="col-md-12 center-inside">
                                                        <button v-if="resource === undefined"
                                                                @click.prevent="submitResource"
                                                                class="btn btn-primary btn-md p-1 mr-1">Submit</button>
                                                        <button v-else @click.prevent="updateResource(resource.id)"
                                                                class="btn btn-primary btn-md mr-1 p-1">Update</button>
                                                        <button @click.prevent="emitCloseModal"
                                                                class="btn btn-danger btn-md p-1">Close</button>
                                                    </div>
                                                </form>

                                            </div>
                                        </div>

                                        <p class="text-center text-danger"
                                           v-for="(error, index) in errors" :key="index">
                                            {{ error}}
                                        </p>

                                    </div>
                                </div>
                            </div>
                        </div>

                    </div>
                </div>
            </transition>
        </div>
    </transition>

</template>

<script>
// import CkEditor in current component to work
import CKEditor from '@ckeditor/ckeditor5-vue';
import ClassicEditor from '@ckeditor/ckeditor5-build-classic';

import {onBeforeMount, onUnmounted, onMounted, reactive, ref, watch} from 'vue';
import SweetAlertService from "../../../../services/sweet-alert-service";
import axios from "axios";
import {useRoute} from "vue-router";

export default {
    props: {
        resource: Object,
        course: Object,
        open: Boolean,
    },

    components: {
        // Use the <ckeditor> component in this view.
        ckeditor: CKEditor.component,
    },

    // Context for emit
    setup(props, context){
        // For routing with params, get id from current url
        const course_id = ref('');
        const route = useRoute();
        course_id.value = route.params.id;
        watch(
            () => route.params.id,
            () => {
                course_id.value = route.params.id;
            },
        );

        const resource = ref(props.resource);
        const course = ref(props.course);
        const courses = ref([]);
        const form = reactive({
            brace_course_id: props.resource !== undefined ? resource.value.brace_course_id : course_id.value,
            title: props.resource !== undefined ? resource.value.title : '',
            url: props.resource !== undefined ? resource.value.url : '',
            document: props.resource !== undefined ? resource.value.document : null,
        });
        const editor = ClassicEditor;
        const editorConfig = ref({});
        const errors = ref([]);
        const submission_error = ref('');
        const documentErrorMessage = ref('');

        const emitCloseModal = () => {
            console.log("emit close modal");
            context.emit("emitting-close-modal", 'Close modal');
        }

        const handleKeyup = (event) => {
            if(event.keyCode === 27){
                emitCloseModal();
            }
        }

        const uploadDocument = (event) => {
            validateDocument(event);
            //Assign document and path to this variable
            form.document = event.target.files[0];
        }

        const validateDocument = (event) => {
            // Validate Document
            let allowedExtensions = /(\.jpg|\.jpeg|\.png|\.doc|\.docx|\.xls|\.xlsx|\.pdf)$/i;
            if(!allowedExtensions.exec(event.target.files[0].name)){
                documentErrorMessage.value = 'Incorrect document format';
                console.log(documentErrorMessage.value);
                form.document = null;
                return false;
            }else{
                documentErrorMessage.value = '';
            }

            if(event.target.files[0].size > 5000000){
                documentErrorMessage.value = 'File too large, 15mb max.';
                form.document = null;
                return false;
            }else{
                documentErrorMessage.value = '';
            }
        }

        const submitResource = async () => {
            SweetAlertService.formLoading(Swal, 'Please wait', 'Submitting...');

            if(form.document === null && !form.url){
                SweetAlertService.formError(Swal, 'Upload a document or add a url', 7000);
                return false;
            }

            if(form.url && !form.url.startsWith('http')){
                SweetAlertService.formError(Swal, 'Insert a full url starting with http', 7000);
                return false;
            }

            const formData = new FormData();
            // iterate and add form data
            Object.keys(form).forEach(function(key) {
                console.log(key); // key
                console.log(form[key]); // value
                if(form[key] !== null){
                    formData.append(key, form[key]);
                }
            });

            await axios.post('/api/admin/course/resources/store', formData, {
                headers: {
                    'content-type': 'multipart/form-data',
                    "Authorization" : "Bearer " + localStorage.getItem('afc-admin-tk'),
                    'Accept' : 'application/json',
                }
            }).then((response) => {
                if (response.data.success){
                    errors.value = [];
                    console.log("Emitting Submitted Resource");
                    context.emit("emitting-submitted-resource", response.data.resource);
                    console.log(response.data.resource);

                    SweetAlertService.formSuccess(Swal, 'Submitted', 2500);
                    // Clear fields
                    Object.keys(form).forEach(function(key) {
                        // If key is not equals to brace_course_id, clear all fields
                        if(key !== 'brace_course_id'){
                            form[key] = '';
                        }
                    });

                } else {
                    if(response.data.submission_error){
                        submission_error.value = response.data.submission_error;
                        SweetAlertService.formError(Swal, response.data.submission_error, 7000);
                    }
                    SweetAlertService.formError(Swal, 'one or more fields are required', 7000);
                    if(response.data.errors.length){
                        errors.value = response.data.errors;
                    }
                }

            }).catch((error) => {
                console.log(error);
            });
        }

        const updateResource = async (id) => {
            if(form.url && !form.url.startsWith('https')){
                SweetAlertService.formError(Swal, 'Insert a full url starting with http', 7000);
                return false;
            }

            const formData = new FormData();
            // iterate and add form data
            Object.keys(form).forEach(function(key) {
                console.log(key); // key
                console.log(form[key]); // value
                if(form[key] !== null){
                    formData.append(key, form[key]);
                }

                // if form.document is same as already uploaded document url, ignore
                if(key === 'document' && form[key] === resource.value.document_path+resource.value.document_name){
                    formData.append(key, '');
                }
            });

            await axios.post('/api/admin/course/resources/update', formData, {
                headers: {
                    'content-type': 'multipart/form-data',
                    "Authorization" : "Bearer " + localStorage.getItem('afc-admin-tk'),
                    'Accept' : 'application/json',
                },
                params: {
                    id: id
                }
            }).then((response) => {
                if (response.data.success) {
                    console.log("Emitting Updated Resource");
                    context.emit("emitting-updated-resource", response.data.resource);
                    console.log(response.data.resource);

                    SweetAlertService.formSuccess(Swal, 'Updated', 2500);
                } else {
                    if(response.data.message){
                        console.log(response.data.message);
                    }
                    if(response.data.errors.length){
                        errors.value = response.data.errors;
                        SweetAlertService.formError(Swal, 'one or more fields are required', 5000);
                    }

                }
            }).catch((error) => {
                console.log(error);
            });
        }

        const getCourses = () => {
            axios.get('/api/admin/courses', {
                withCredentials: true,
                headers: {
                    "Authorization" : "Bearer " + localStorage.getItem('afc-admin-tk'),
                    'Accept' : 'application/json',
                },
            }).then((response) => {
                if(response.data.success === true){
                    courses.value = response.data.courses.data;
                }else{
                    console.log(response.data.message);
                }
                console.log(courses.value);
            }).catch((error) => {
                console.log(error);
            });
        }

        onBeforeMount(() => {
            document.addEventListener("keyup", handleKeyup);
            if(resource !== undefined){
                getCourses();
            }
        });

        onUnmounted(() => {
            document.removeEventListener("keyup", handleKeyup);
        });

        return {
            resource,
            course_id,
            route,
            form,
            courses,
            course,
            editor,
            editorConfig,
            errors,
            submission_error,
            documentErrorMessage,

            submitResource,
            updateResource,
            emitCloseModal,
            getCourses,
            validateDocument,
            uploadDocument
        }
    }
}
</script>

